// Functions are types of objects that can be called to perform actions or compute values.
// Functions can take parameters and return values, and they can be defined in various ways, including
// function declarations, function expressions, and arrow functions.    

// Example: A simple function that adds two numbers
function add(a: number, b: number): number {
    return a + b;
}       

// It is better to annotate the function parameters and return type to ensure type safety and clarity.
// In the above example, the number at the end of the function signature indicates that the function will return a number.
